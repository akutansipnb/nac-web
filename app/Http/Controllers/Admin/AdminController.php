    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $icon = $request->file('img_url');
        $icon_name = strtolower(str_replace(' ', '', $request->name)).".".$icon->getClientOriginalExtension();

        $request->validate([
            'name'=>'required',
            'email'=>'required|unique:users,email',
            'password'=>'required,min:8'
        ],[
            'email.unique' => "Data Sudah Ada !"
        ]);

        try {
            User::create([
                'name' => $request->name,
                'email' => $request->email,
                'password' => bcrypt($request->password),
                'img_url' => 'img/profile/admin/'.$icon_name
            ])->assignRole('Administrator');

            // Moves Files
            $icon->move('img/profile/admin',$icon_name);
            return redirect()->route('register.index');
        } catch (\Throwable $th) {
            throw $th;
        }
    }

